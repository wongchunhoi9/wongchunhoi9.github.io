                    <h4>Concept</h4>
                    <p>
                        How does “the future” as it has been imagined by our predecessors differ from that in contemporary thought? Have we really “become” one of the futures from the past? The Dream Machine is an installation that features a machine-based collage of dreamscapes developed in response to Chang Shi-kuo’s “Dream Snippers” from his collection of short stories, Nebula Suite. Extending the concept of Retro-futurism, the installation uses a media archeological approach: we integrate audiovisual contents into a spectrum of technical devices, analogue and digital, a reimagined system that echoes the original short story. The whole system can be described as second-order cybernetics with the theme of dream recurring in the individual units of the modular system. By fusing a spectrum of machines, of analogue and digital mediums (e.g. audiovisual, text, programming), with a patching system, the installation seeks to propose a possible reading of the original short story. It addresses the specific content of dreams and personal identities as audiovisual content. It raises issues of autonomy, censorship and surveillance through processual rules and principles embedded in our design of panel control. We highlight the cross-disciplinary requirement in setting up a dream surveillance room — through the design of interfaces, consoles and the overall connectivity of the installation.
                    </p>
                    <p> Our on-site exposition begins by describing the conceptual starting points (input) of the system, followed by details of how the system works and its overall mechanical structure in each of the modules. The endpoint of the physical installation will be the closing summary of our findings.</p>
                    <p> What is it to dream? What desires drive us to dreaming? Why do we dream at all? In lieu of raising arguable, and perhaps predictable, questions such as these, the discourse of this installation inclines to a more nuanced, complex investigation of what we might ask of dreaming and machines, and asks, instead, the following questions:</p>
                    
                    
                    <ul>
                        <li>What triggers our attentiveness towards the state of dreaming? </li>
                        <li>What might be the possible ideologies implied by the dream snippers?</li>
                         <li>What is the role of such a machine in terms of altering and generating a possible future within a human mind?</li>
                    </ul>
                    
                    <p>
                    The composite installation, The Dream Machine, brings together a series of dreamscapes that provoke experimentations beyond the realm of intuitive imagination. With varied media formats such as animation, video and text, we delve into the rich qualities and multiple layers of dreams which become the dream sequences visitors see. Some dreams are “ideal situations” with reference to our observation of everyday life, some bring forth abstract textures, or form visual motifs. Some raise an alternative way of looking at dreams, as when visual perception is no longer restricted to the structure of our eyes. These elements form a mosaic for stretching the visitors’ assumptions about dreams, tying back to the theme of struggle between the subject of imagination, art and controlled dreaming in the original story. </p>
                    <p>It is crucial to note that the audiovisual content in The Dream Machine does not suggest a direct simulation nor interpretation of the worldview in Chang’s story but, rather, an adaptive machine as the artists reimagine it. With analog and digital systems working together, a cluster of inputs (formerly noted as currents) illuminate the procedural state of the machine and its system flow.</p>
                    <p>Apart from constructing the multiple latitudes of a dream, another critical aspect of this installation constitutes a control room with a monitoring panel for the surveillance of dreams. Piecing together a possible reading of the literary work as a basis, and regulations from the Hong Kong Information Technology and Broadcast Bureau, a set of principles and rules is established for the operation of the control panel. Despite the concise instructions, it cannot exist without an element of doubt, knowing there is also the presence of a dream snipper. What is it to be a professional dream snipper? When is the perfect moment to unleash the dragon and cover up dream scenarios which are crossing the line? What is the reason behind a design that allows censoring sound bites in these situations? Is it just a beep, or could it be a more characteristic sound effect that smoothly obscures the border between dream and reality without awakening the users? The planning and arrangement of this panel not only aims at creating a room for reading, but also tries to raise a hypothetical passage, almost a proposal of an alternative aesthetics, in shaping and sculpting a dream through the act of snipping in both the visual and the audio aspect.</p>
                    <p>Artistically, technically and structurally speaking, this composite installation is a modular machine system, divided into five modules (sub-systems). The first one is a household module, referencing the original story’s depiction of how dreamers with a subscription plug themselves into the network from home. Next is the surveillance panel, which carries a programme that processes audiovisual materials, allowing the users to listen to the censored dreams.  The third and main module is the control room, the central processor of the installation, from where dreams are distributed (or delivered) to the monitors and screens of the entire installation. The panel features an analog patching machine that connects with a video mixer. This provides a tactile experience for the visitors. The fourth module is the display of dreamscapes, which contains partly a projection on a curtain, as well as a structure featuring a rig of monitors. Each zone acts as an anchor to bridge smaller components into the final assemblage of the whole system, which provides the audience with different perspectives in engaging the idea of dream discussed in the story. The artistic re-imagination of these modules draws from the original story whereby dreaming (or, plugging in to a dream console) becomes a form of consumption and mass social media service. The formal set-up of the machine can also be understood as a mixed media installation with purposeful eclectic combinations of different media and hardware materials. The design of each module, or zone, is the result of team members’ dynamic collaboration through discussions and on-site trial and error of physical assemblage of installation components, a process in which concepts such as machine aesthetics, software art, the media art of moving images and sound art were scrutinized and thoroughly worked through.</p>


                    <p>我們當下所想像的「未來」 與前人有所不同嗎？我們真的成了昔日的「未來」的一部分嗎？《夢幻機器》以一組由機械與夢境拼貼組裝而成的裝置回應張系國《星雲組曲》一作中的「翦夢奇緣」。這組裝置意圖運用媒體考古的方式延伸對復古未來主義(Retro-futurism) 的一些想像：將視聽內容融合一系列數位與類比的技術設備構建一組假定的系統，與短篇故事互相呼應。整個系統可以被描述為以二階控制論配合「夢」為題的多頻道錄像重複出現於模塊化系統的各個單元。</p>
                    <p>這組裝置透過挪用機械、種種數位與類比媒介(視聽材料、文本、電腦編程)，以及連繫各元素的修補系統，嘗試對原著提出一種可能的閱讀方向。作品中的視聽材料剖析各種對夢與自我的思考，裝置中的建構規劃和設計原理隱示一些關於自治、審查和監控的問題。從介面設計、控制台的設置，以至各個模塊化單元當中的連繫性，《夢幻機器》這組裝置無疑以各種跨媒介的協作強調媒體設計中環環相扣的狀態。</p>
                    <p>裝置的現場展示，以描述系統的概念起點（輸入）為首，再詳細說明系統如何與各模塊化單元結連，整體上的機械建構與安裝則會連繫我們對原著小說「翦夢奇緣」的幾個關注點收結。</p>
                    <p>甚麼是造夢？甚麼驅使我們想造夢？為甚麼我們會造夢呢？與其拘泥於這些無法總結，又或嘗試推敲種種可預測的陳述，這組裝置更傾向釐清我們對造夢或機器的一些更細緻與複雜的考究，包括：</p>
                    <ul>
                        <li>是甚麼促使我們對造夢狀態的關注和好奇？</li>
                        <li>我們能否從被審查的夢境片段中推敲到一些指向翦夢者的意識形態的閱讀？ </li>   
                        <li> 這組機器以潛入人類思維而改變或創造另一個「未來」的定位是什麼？</li> 
                    </ul>        
                 
                    <p>《夢幻機器》以模塊化單元整合的機械裝置匯集了一系列夢幻景觀，以影像概括了一些跨越直覺想像的視覺實驗，以各種媒體格式（如動畫，視頻和文字）探索夢景的複雜性及多層次性，形成眼前顯示的模擬夢境序列。有些「夢境」源於我們對日常生活觀察所幻想的「夢想狀態」，有些是抽象紋理，又或形成視覺基序，有些嘗試提出一種直視夢的方式，猶如視覺感知不再局限於我們眼睛的結構一樣。這些元素形成了一組馬賽克，擴張觀眾對夢的假設，回溯到原著中種種對想像，藝術和控制夢境之間的拉扯。
                    值得注意的是，《夢幻機器》中的視聽內容並非要直接模擬或嘗試詮釋故事中的世界觀，而是作為一種輸入機械裝置的資訊，以致當數位與類比系統協作的情況下，這些視聽內容可辨明機器程序的狀態與其系統流程。</p>

                    <p>除了建構夢景的多緯度空間外，另一關鍵的側重點便是裝置內的模擬控制室，以及室內的夢境監視系統。系統的運作模式與規則根據原著作為參考基礎，再按照香港信息技術和廣播局的規定想像一套曾不存在的規條。儘管確立了這些簡潔的指示，翦夢者的存在無疑暗示系統並非追求完美精確的狀態。甚樣可以成為一名專業的翦夢者？何時是釋放龍的最佳時刻，掩蓋了有違道德的夢景？龍的畫面與自主規制音的作用對於翦夢有甚麼作用呢？自主規制音的音源有從何而來？音源本身是一個「嗶」聲，還是可以容許一個更具特色的聲音效果，可以在不喚醒用戶的情況下平滑地模糊夢想與現實之間的界限呢？這套夢境監視系統的規劃和安排不僅旨在創造一種閱讀原著的方向，當中通過以視覺和音頻剪接與重塑夢境的手法，幾乎試圖提倡一套另類的美學。</p>
                    <p>從美學、技術和結構上來說，這種複合裝置是一組模塊化機器系統，分為五個模塊（子系統）。首先是家居模塊，透過參考原著的描述，這單元模擬訂閱者如何在家中連繫網絡。第二是監控模塊，這模塊帶有一個處理視聽內容的編程，允許用戶聽取被審查的夢境。第三以及主要模塊則是控制室，也是複合裝置的中央處理器，負責將夢境分配或傳達至各個監視介面和屏幕。控制室具有一組連繫視頻混合器的類比修補機器而供模擬翦夢者的操作模式。第四組模塊是一組夢景的展示幕，當中亦包含各種影像介面的裝置結構。每組模塊各自都是整套複合裝置的錨點，將較小的組件整合橋接到整個系統之中，從而容許觀眾透過不同視角參與討論。透過抽取對原著的一些想像裝嵌這些模塊塑造夢（或接通夢想控制台）成為一種消費與大眾社交媒體服務的形式。</p>
                    機器的設置理論上可以解釋為以混合媒介與各種數位與類比設備拼貼而成的機械裝置。每個模塊區域的設計均是團隊成員通過討論、試驗、安裝組件的物理組合錯誤進行動態協作的結果，記錄了一個對於機器美學、軟件、媒體藝術（動態圖像和聲音處理）等種種概念進行仔細檢查並徹底解決的過程。</p>


