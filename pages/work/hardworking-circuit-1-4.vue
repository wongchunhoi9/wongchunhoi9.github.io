                    <h5>勤力電路1.4 – 開著了的投影機 (持續創作)》 </h5>
                    
                    <p>王鎮海的裝置作品《勤力電路》是一個以繼電器串連的系統。繼電器是以電力操控的開關，當電流通過銅線，銅線產生磁場閉合繼電器中的電路，組成電流傳輸的橋樑，繼而觸發下一個繼電器。觀眾可透過手動開關，自由操控骨牌似的電路系列。《勤力電路1.4》以藝術家之前的創作《勤力電路1》作為骨幹。作品結構以多個繼電器串聯而成，並以骨牌的形式相繼啟動，一個接一個，直到終端電器被開啟。重覆設置於電路上的繼電器具電子時計的作用，最後被選擇啟動的電器是藝術性和趣味的關鍵。開關按鈕後觸發的每一個步驟相對於電影的情節，像微敍事，觀眾觀看著電力飛舞的定格及藝術家舖墊的最終結局。電力在媒體藝術是必然的存在，王氏再一次以電力為藝術的原材料，並呈現電力在不同物理形式上的操控。</p>

                    <p>在《勤力電路#1》、《勤力電路#1.1》、和《勤力電路#1.2》中，電路系統雖然有著不同設計，但電路最終都只為傳輸電流，以啟動瑣碎物件，如一顆小型LED燈、一隻以摩打驅動的玩具魚、一個改裝音樂盒等。透過改變平日的電路流程，耗時又吃力的勤力電路希望提升人類對尋常瑣物的興趣。王氏稱這些終端物件為系統的結局。當繼電系統開始運作，一連串的動作隨繼電過程而觸發，終端物亦最終受改變。王氏所設計的結局，如閃動的燈和震動的魚，吸引人們的目光，直至過程終結。王氏一直持續研究不同電子零件以製造不同的結局。技術上，將大部分的備開關的電器放置於電路上任何繼電的一點，都能被「啟動」。</p>

                    <p>電流訊號的流動是耗時的過程。在版本1.4 中，作品強調繼電過程的時限及持續時間。以《勤力電路1.2》為例，每4.5 秒，電流平均通過360 個繼電開關。這是藝術家展開勤力電路最新版本的基礎。最新版本中，電路上1秒能把80個繼電器逐一啟動。這種計算連繫到我們對電影時間的認知，由傳統以1秒24格為標準的菲林影片徹底改變成一部1秒80幀的影像投影機。對此王氏以電影作典故，並嘗試在概念上開拓其形式。問題亦繼續延續下去：觀眾如何體驗這個1 秒80 格的視覺呈示？觀眾如何描敍在作品中的時間經驗？在眾多的5 秒繼電故事裡，觀眾得到了什麼？他們在連續而無間斷的5 秒情節入面，會建構出怎樣的故事？</p>

