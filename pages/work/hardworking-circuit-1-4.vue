                    <h5>Wong Chun-hoi’s hardworking circuit #1.4 – A switched-on projector [work-in-progress] (2018)</h5>
                    <p>A circuit physically functions as a sequence of successive on-off switches that form a domino structure. A bunch of switches, turned on sequentially in serial ordering, will “terminate” after turning on all electronic/everyday objects one by one. The selection of objects to be turned on the sequence is the key to discovering the playful artistic possibilities afforded by the timeframe generated by the circuit’s physical manifestation. Each iteration, ignited by pressing the “on” button, initiates a process comparable to a movie sequence unfolding in the form of x frames per second, like a micro narrative, whereby the audience study the causal drama of electricity dancing through within a set timeframe with a visible, spectacular process and a curious “denouement” [結局、終局].</p>

                    <p>The repetitive relay designed into the circuit ends with an electronic clock, the “denouement,” whereas the conspicuous objects along the relay are suggested or revealed on a TV monitor connected to hidden web cams in various parts of the venue. While electricity has always been taken for granted in media art, Wong takes up electricity as an artistic raw material, to perform its manifestation in many physical forms.It takes several seconds to turn on all sections of relay successively one by one. With this, Wong preserves his work’s allusion to cinema in a conceptually expanded form. The question remains: how do visitors experience this procedural visual presentation? How would they describe their experience of time in this work? What would visitors get out of each of the many 5-second relay narratives in the show? And what about a narrative they may construct on the spot with many continuous, consecutive 5-second segments?</p>

                    <p>The hardworking circuit series began in 2015 as experiments, published at Oil Street and the Floating Projects. hardworking circuit 1.3 has won the Gold Award, Media Art category in the ifva 2018 competition. Gao Shiqiang, one of the jurors, commented, “This artist attempts to delay the completion of the route from A to B, and makes something out of this clever idea. The root itself is very complicated, and reflects what our daily lives are about, that you often have to go through a lot of things before you get to the final destination.”</p>

