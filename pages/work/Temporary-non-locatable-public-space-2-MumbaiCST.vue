                   <h4>後記（一）</h4>
                    <p>FM96.7， 據維基百科，在孟買的電台，這是還未有被佔用的頻道。</p>
                    <p>我不肯定在印度在孟買的電台管制是怎樣，反正就是拿了一個10W功率的FM發射器去，相信應該沒有一個國家的法律是正式容許地下電台的。只是感覺在印度做非法電台比起在香港應該會輕鬆一點。</p>
                    <p>恰巧，在車站很多走過來好奇的路人，都會拿起電話戴上耳塞，試試在電話的收音機功能，用上自動搜台功能偶然會成功找到此「no name」頻道。很多人仍是在用Nokia的有按扭的電話。</p>
                    <p>電台節目的素材：</p>
                    <ul>
                    <li>五天前前後後跟二十組人的訪問</li>
                    <li>坐火車時的錄音，有很擠的，也有很鬆動的</li>
                    <li>在火車站的環境聲</li>
                    <li>用了二十盧比買回來的擦鞋箱聲音</li>
                    <li>連續的節日，在街上載歌載舞的音樂 （成了電台節目的背景音樂）</li>
                    </ul>
                    <p>我這次的設置也是把所有東西用具都放上桌子，我想其實FM廣播的方便也是備受冷落和被視為過時的，也很難想像是如此簡單。</p>
                    <p>基於物理的限制，FM製造了一條邊界，好像建構了一個世界（為我自己），在那裡你可以隨意放大某個聲音，再縮小到某知音的耳邊。</p>
                    <p>強行與現今最「流通」、「自由」的互聯網技術比較，其實我也許是可以用無線路由器去建立內聯網的而跳過ISP的，同樣也是要求一個主動登入的姿勢。但總是覺得電腦電話還是太接近各人自己的世界。收音機則是從手感上給予專注的力量，還有那瞎子摸象似的感覺和好奇心的橋樑。</p>
                    <p>我想我應有個堅持是要做直播，是要在那個公共藝術發生的地方做直播。</p>
                    <p>想知道這個FM 系統的出現 和我的身體可以在這空間換取到什麼樣的資訊。</p>
                    <p>自問不是一個好的電台主持，比起自己把聲，我更放心讓其他聲音自己去被人聆聽，但出現了「開放論壇」 是意料之外的。</p>
                    <p>在第二天做直播的大約十一時左右，一堆路過的男人圍在旁電台旁，因為見到有咪高風存在，我也有幾個收音機在附近，相信他們也意識到這是個傳播的系統。其中一個男人要求想用說點東西（say something），我給他簡單的指示叫他說說關於這個車站的事，他問我可不可以用印度文講，我說：「請」。</p>
                    <p>那一刻其實有點恐懼的，因為一來真的不太了解他在說什麼，但是聽到「Pakistan」同「India」&nbsp;&nbsp; ，我知道兩國關係一直麻麻，也知道是長久在兩地的政治議題，只是真的不知會否引起爭執，因為我真的不知道旁人是用一個什麼心態去看待一支不知在幹什麼的咪高風，只是拿起咪高風，就覺得那個空間的聲音可以傳得很遠。他「演說」完畢，很有禮貌面帶笑容地說「thank you」，然後有一個自稱來自尼泊爾的男人又想講話，我便開始擔心不受控制變了城市論壇，眼前這堆印度版的維圍阿伯們越講越興奮，卻只有作為「主持」的我完全不知遁他們在說什麼，只能憑少量聽到單字去估。</p>
                    <p>我那刻的恐懼應該是源於我怕會發生控制不了的爭執，我想這也就是政權要控制輿論的原因之一吧。</p>
                    <p>只是當尼泊爾男人很激動地說完那個演說後，立即變回一位很文雅之人然後溫柔地說「thank you」。然後人群散去，一個正前往Safety officer面試的年青的男子才幫忙解釋剛剛的印度文演說。</p>
                    <p>巴基斯坦跟印度的關係 ，相信他們很關心這個問題。</p>
                    <p>我當時淆底，真的只是怕會給展覽主辦方惹上麻煩，然後人在外地生路不熟，惹來真警察的話看見那個FM 發射器相信定必解釋到佛誕都不知如何是好。只是在後來的一段時間，在車站的保安警察也來問如何用電話聽得到我的電台，於是我教他不要用電話的自動捜台。</p>
                    <p>另一方面也是在想過到底我有沒有熄咪的權力 ，或是自拆大台，叫大家收檔。</p>
                    <p>作品這一段， 我相信這是其中一個在孟買一役最深刻的。也否定了一些對「公共空間」曾經有過的意義及想像。</p>
                    <p>其後有個很長氣的伯伯跟我傾科技的事，說想要一種好穩定不會斷線的無線電。</p>
                   
                  
